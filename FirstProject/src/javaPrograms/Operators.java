package javaPrograms;

public class Operators {

	public static void main(String[] args) {
		
		
		int x=5,y=6;
		//increment operator
		System.out.println("incremented value of x is "+ ++x);
		System.out.println("incremented value of x is "+ x++);
		
		//decrement operator
		System.out.println("decremented value of y is "+ --y);
		System.out.println("decremented value of y is "+ y--);
		
		//Arithmetic operators
		
		int a=400,b=200;
		System.out.println("Output for Arithmetic operators :");
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		//Relational Operators
		int xx=20,yy=30;
		System.out.println("Output for Relational operators :");
		System.out.println(xx!=yy);
		System.out.println(xx>yy);
		System.out.println(xx<yy);
		System.out.println(xx>=yy);
		System.out.println(xx<=yy);
		
		System.out.println(xx=yy);
		
		
	}

}
